//message, statusCode, errorCode, error

export class HttpException extends Error {
  message: ErrorMessages; //properties
  statusCode: number;
  errorCode: ErrorCode;
  errors: unknown;
  constructor(
    message: ErrorMessages,
    statusCode: number,
    errorCode: ErrorCode,
    errors: unknown
  ) {
    super(message);
    this.message = message;
    this.statusCode = statusCode;
    this.errorCode = errorCode;
    this.errors = errors;
  }
}


